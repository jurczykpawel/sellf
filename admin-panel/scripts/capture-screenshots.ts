  const { data: products } = await supabaseAdmin
    .from('products')
    .select('id, name, price')
    .in('name', ['Premium Course', 'Pro Toolkit', 'VIP Masterclass', 'Community Guide', 'Design System Bundle']);

  if (!products?.length) {
    console.warn('No products found for demo transactions');
    return;
  }

  // Create demo users for realistic user count
  const demoUserIds: string[] = [];
  for (let i = 0; i < 40; i++) {
    const { data: { user } } = await supabaseAdmin.auth.admin.createUser({
      email: `demo-user-${i}@sellf-demo.test`,
      password: 'demo123',
      email_confirm: true,
    });
    if (user) demoUserIds.push(user.id);
  }

  // Insert realistic payment transactions spread over 30 days
  const now = new Date();
  const transactions: Array<{
    user_id: string;
    product_id: string;
    amount: number;
    currency: string;
    status: string;
    stripe_payment_intent_id: string;
    session_id: string;
    customer_email: string;
    created_at: string;
  }> = [];

  const names = ['Alice', 'Bob', 'Charlie', 'Diana', 'Eve', 'Frank', 'Grace', 'Henry', 'Ivy', 'Jack',
    'Kate', 'Leo', 'Mia', 'Noah', 'Olivia', 'Paul', 'Quinn', 'Rosa', 'Sam', 'Tara'];

  for (let dayOffset = 30; dayOffset >= 0; dayOffset--) {
    // More transactions in recent days (growth curve)
    const txCount = dayOffset > 20 ? 1 : dayOffset > 10 ? 2 : dayOffset > 5 ? 3 : 5;
    for (let j = 0; j < txCount; j++) {
      const product = products[Math.floor(Math.random() * products.length)];
      if (!product.price || product.price === 0) continue;
      const userIdx = Math.floor(Math.random() * Math.min(demoUserIds.length, 20));
      const date = new Date(now);
      date.setDate(date.getDate() - dayOffset);
      date.setHours(Math.floor(Math.random() * 14) + 8); // 8am-10pm

      const name = names[userIdx % names.length];
      transactions.push({
        user_id: demoUserIds[userIdx] || demoUserIds[0],
        product_id: product.id,
        amount: Math.round(product.price * 100), // DB stores cents
        currency: 'USD',
        status: 'completed',
        stripe_payment_intent_id: `pi_demo_${dayOffset}_${j}_${Date.now()}`,
        session_id: `cs_demo_${dayOffset}_${j}_${Date.now()}`,
        customer_email: `${name.toLowerCase()}@example.com`,
        created_at: date.toISOString(),
      });
    }
  }

  // Insert in batches
  for (let i = 0; i < transactions.length; i += 20) {
    const batch = transactions.slice(i, i + 20);
    await supabaseAdmin.from('payment_transactions').insert(batch);
  }

  console.log(`  Inserted ${transactions.length} demo transactions, ${demoUserIds.length} demo users`);
