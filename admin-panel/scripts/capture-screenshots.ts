// ===== Auth helpers =====

async function createAdmin() {
  const email = 'admin@sellf-demo.com';
  const password = 'password123';

  // Reuse existing user if already created from a previous run
  const { data: { users } } = await supabaseAdmin.auth.admin.listUsers({ perPage: 1000 });
  const existing = users?.find((u) => u.email === email);

  if (existing) {
    // Ensure admin role exists
    await supabaseAdmin.from('admin_users').upsert({ user_id: existing.id }, { onConflict: 'user_id' });
    return { email, password, userId: existing.id };
  }

  const { data: { user }, error } = await supabaseAdmin.auth.admin.createUser({
    email,
    password,
    email_confirm: true,
  });
  if (error) throw error;

  await supabaseAdmin.from('admin_users').insert({ user_id: user!.id });

  return { email, password, userId: user!.id };
}

async function setAuthCookies(context: import('playwright').BrowserContext, email: string, password: string) {
  const signInClient = createClient(SUPABASE_URL, ANON_KEY);
  const { data: { session }, error } = await signInClient.auth.signInWithPassword({ email, password });
  if (error || !session) throw new Error(`Sign-in failed: ${error?.message || 'no session'}`);

  const cookieKey = `sb-${new URL(SUPABASE_URL).hostname.split('.')[0]}-auth-token`;
  const base64Value = `base64-${Buffer.from(JSON.stringify(session)).toString('base64url')}`;

  const CHUNK_SIZE = 3180;
  if (base64Value.length <= CHUNK_SIZE) {
    await context.addCookies([{ name: cookieKey, value: base64Value, domain: 'localhost', path: '/' }]);
  } else {
    const chunks: string[] = [];
    for (let i = 0; i < base64Value.length; i += CHUNK_SIZE) {
      chunks.push(base64Value.substring(i, i + CHUNK_SIZE));
    }
    await context.addCookies(chunks.map((chunk, idx) => ({
      name: `${cookieKey}.${idx}`, value: chunk, domain: 'localhost', path: '/',
    })));
  }
}

async function setConsentCookie(context: import('playwright').BrowserContext) {
  await context.addCookies([{
    name: 'sellf_consent',
    value: JSON.stringify({ 'google-tag-manager': true, 'facebook-pixel': true, 'umami-analytics': true }),
    domain: 'localhost',
    path: '/',
  }]);
}

// ===== Screenshot capture =====

/** CSS injected BEFORE page load to hide noise */
const HIDE_NOISE_CSS = `
  #klaro, .klaro { display: none !important; }
  [class*="bg-sf-danger-soft"] { display: none !important; }
  nextjs-portal { display: none !important; }
  [data-nextjs-toast] { display: none !important; }
  #__next-build-watcher { display: none !important; }
  [class*="nextjs-"] { display: none !important; }
  body > div[style*="position: fixed"][style*="bottom"] { display: none !important; }
`;

async function capture(
  context: import('playwright').BrowserContext,
  pages: PageSpec[],
) {
  const page = await context.newPage();

  // Inject CSS + JS before any navigation to hide dev noise
  await page.addInitScript((css) => {
    const inject = () => {
      if (document.head) {
        const style = document.createElement('style');
        style.textContent = css;
        document.head.appendChild(style);
      } else {
        setTimeout(inject, 10);
      }
    };
    inject();

    // Remove Next.js dev indicator (uses Shadow DOM, CSS can't target it)
    const removeDevIndicator = () => {
      document.querySelectorAll('nextjs-portal').forEach(el => el.remove());
      // The "N Issues" badge
      const allElements = document.querySelectorAll('body > *');
      allElements.forEach(el => {
        if (el.shadowRoot || el.tagName?.toLowerCase().includes('next')) {
          el.remove();
        }
      });
    };
    // Run after page settles
    setTimeout(removeDevIndicator, 1500);
    setTimeout(removeDevIndicator, 2500);

    // Note: sf_sidebar_pinned is set via context.addInitScript in main()
  }, HIDE_NOISE_CSS);

  let sidebarPinned = false;

  for (const { path: pagePath, filename, afterLoad } of pages) {
    const url = `${BASE_URL}/en${pagePath}`;
    console.log(`  ${filename} → ${url}`);

    await page.goto(url, { waitUntil: 'domcontentloaded', timeout: 30_000 });
    // Settle time for React hydration, charts, lazy content
    await page.waitForTimeout(3000);

    // Pin sidebar open on first dashboard page (persists via localStorage for subsequent pages)
    if (!sidebarPinned && pagePath.startsWith('/dashboard')) {
      const sidebar = page.locator('aside').first();
      await sidebar.hover();
      await page.waitForTimeout(400);
      const pinBtn = sidebar.locator('button[aria-label="Pin sidebar open"]');
      if (await pinBtn.isVisible()) {
        await pinBtn.click();
        await page.waitForTimeout(600);
        sidebarPinned = true;
      }
    }

    // Run page-specific actions (e.g., filter dropdowns)
    if (afterLoad) {
      await afterLoad(page);
    }

    // Remove Next.js dev indicator (Shadow DOM element at bottom-left)
    await page.evaluate(() => {
      document.querySelectorAll('nextjs-portal').forEach(el => el.remove());
      document.querySelectorAll('*').forEach(el => {
        if (el.tagName?.toLowerCase().startsWith('nextjs')) el.remove();
      });
    });

    await page.screenshot({
      path: path.join(OUTPUT_DIR, filename),
      fullPage: false,
    });
  }

  await page.close();
}

// ===== Main =====

async function main() {
  await setupDemoData();

  console.log('\nCreating screenshot admin...');
  const admin = await createAdmin();

  const browser = await chromium.launch();

  try {
    // ===== Light theme =====
    console.log('\nCapturing light theme (2x retina)...');
    const lightContext = await browser.newContext({
      viewport: { width: 1920, height: 1080 },
      deviceScaleFactor: 2,
      colorScheme: 'light',
    });
    await lightContext.addInitScript(() => {
      localStorage.setItem('sf_sidebar_pinned', 'true');
    });
    await setConsentCookie(lightContext);
    await setAuthCookies(lightContext, admin.email, admin.password);
    await capture(lightContext, LIGHT_PAGES);
    await lightContext.close();

    // ===== Dark theme =====
    console.log('\nCapturing dark theme (2x retina)...');
    const darkContext = await browser.newContext({
      viewport: { width: 1920, height: 1080 },
      deviceScaleFactor: 2,
      colorScheme: 'dark',
    });
    await darkContext.addInitScript(() => {
      localStorage.setItem('sf_theme', 'dark');
      localStorage.setItem('sf_sidebar_pinned', 'true');
    });
    await setConsentCookie(darkContext);
    await setAuthCookies(darkContext, admin.email, admin.password);
    await capture(darkContext, DARK_PAGES);
    await darkContext.close();

  } finally {
    await browser.close();
  }

  // Optimize PNGs
  console.log('\nOptimizing PNGs with pngquant...');
  try {
    execSync(`pngquant --quality=80-100 --force --ext .png ${path.join(OUTPUT_DIR, '*.png')}`, {
      stdio: 'inherit',
    });
    console.log('Optimization complete.');
  } catch {
    console.warn('pngquant not found or failed — skipping optimization.');
  }

  console.log(`\nDone! Screenshots saved to: ${OUTPUT_DIR}`);
}

main().catch((err) => {
  console.error('Screenshot capture failed:', err);
  process.exit(1);
});
